                    scrollTopBtn.classList.add('show');
                } else {
                    scrollTopBtn.classList.remove('show');
                }
            }

            // 2. Scroll Spy (Highlight active nav link)
            let current = '';
            sections.forEach(section => {
                const sectionTop = section.offsetTop;
                // Offset calculation (150px) to match the fixed navbar and padding
                if (window.scrollY >= sectionTop - 150) {
                    current = section.getAttribute('id');
                }
            });

            // 3. Check if at the absolute bottom of the page to highlight the last section
            if ((window.innerHeight + window.scrollY) >= document.documentElement.scrollHeight - 10) {
                current = sections[sections.length - 1].getAttribute('id');
            }

            navLinksList.forEach(a => {
                a.classList.remove('active');
                if (current && a.getAttribute('href') === '#' + current) {
                    a.classList.add('active');
                }
            });
        });

        scrollTopBtn.addEventListener('click', () => {
            window.scrollTo({
                top: 0,
                behavior: 'smooth'
            });
        });
        
        // Trigger scroll event on load to highlight the first section initially
        window.dispatchEvent(new Event('scroll'));
